x = 2025
n = 0
while x > 0:
    n += 1
    if n % 2 == 0:
        if n % 3 == 1:
            x -= 7 + 2
        elif n % 3 == 2:
            x -= 7 + 10
        elif n % 3 == 0:
            x -= 7 + 7
    else:
        if n % 3 == 1:
            x -= 20 + 2
        elif n % 3 == 2:
            x -= 20 + 10
        elif n % 3 == 0:
            x -= 20 + 7
print(n)

# 简化版
hp = 2025
n = 0
while hp > 0:
    n += 1
    # 合并英雄1+2的伤害：奇数20，偶数7
    d12 = 20 if n % 2 else 7
    # 英雄3的伤害：按n%3映射（1→2，2→10，0→7）
    d3 = [7, 2, 10][n % 3]  # ！！！索引0对应%3=0，1对应%3=1，2对应%3=2
    hp -= d12 + d3
print(n)  # 输出103